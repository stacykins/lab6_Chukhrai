module ref_sum (Ain, Bin, Ci, Sout, Co);
    input [7:0] Ain, Bin;
    input Ci;
    output [7:0] Sout;
    output Co;
    reg [8:0] temp_sum;

    always @(*) begin
        temp_sum = Ain + Bin + Ci;
    end

    assign Sout = temp_sum[7:0];
    assign Co = temp_sum[8];
endmodule
